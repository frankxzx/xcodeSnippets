/*修改 UITableview 滚动条颜色的方法



UITableview 的滚动条默认颜色是黑色的，如果 UItableview 背景也是深颜色，则滚动条会变的很不明显。您可以用下面这行代码来改变滚动条的颜色

*/

self.tableView.indicatorStyle=UIScrollViewIndicatorStyleWhite;